import fs from "node:fs/promises";
import path from "node:path";
import { gzipSync } from "node:zlib";

const root = process.cwd();
const sourceRoot = path.join(root, "vendor", "phosphor-icons", "SVGs Flat");
const outPath = path.join(root, "src", "BundledPhosphor.ts");
const variants = ["thin", "light", "regular", "bold", "duotone", "fill"];

// The generated bundle is checked in and is the normal build input. Avoid
// materializing thousands of iCloud-evicted vendor files unless an icon-source
// update explicitly requests regeneration.
if (!process.argv.includes("--force")) {
  try {
    if ((await fs.stat(outPath)).size > 0) process.exit(0);
  } catch {
    // Generate the bundle when it is missing.
  }
}

const bundled = {
  name: "Phosphor Icons",
  variants: {},
};

for (const variant of variants) {
  const folder = path.join(sourceRoot, variant);
  const entries = await fs.readdir(folder, { withFileTypes: true });
  const svgs = {};
  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith(".svg")) continue;
    let slug = entry.name.replace(/\.svg$/i, "");
    const suffix = `-${variant}`;
    if (slug.endsWith(suffix)) slug = slug.slice(0, -suffix.length);
    svgs[slug] = await fs.readFile(path.join(folder, entry.name), "utf8");
  }
  bundled.variants[variant] = Object.fromEntries(Object.entries(svgs).sort());
}

const source = [
  "/* Generated by scripts/generate-bundled-phosphor.mjs. Do not edit directly. */",
  `const BUNDLED_PHOSPHOR_GZIP_BASE64 = "${gzipSync(JSON.stringify(bundled), { level: 9 }).toString("base64")}";`,
  "",
  "export interface BundledPhosphor {",
  "\tname: string;",
  "\tvariants: Record<string, Record<string, string>>;",
  "}",
  "",
  "let bundledPhosphor: BundledPhosphor | null = null;",
  "",
  "export async function loadBundledPhosphorData(): Promise<BundledPhosphor> {",
  "\tif (bundledPhosphor) return bundledPhosphor;",
  "",
  "\tconst compressed = Uint8Array.from(",
  "\t\tatob(BUNDLED_PHOSPHOR_GZIP_BASE64),",
  "\t\tcharacter => character.charCodeAt(0),",
  "\t);",
  "\tconst stream = new Blob([compressed]).stream().pipeThrough(new DecompressionStream('gzip'));",
  "\tbundledPhosphor = JSON.parse(await new Response(stream).text()) as BundledPhosphor;",
  "\treturn bundledPhosphor;",
  "}",
  "",
].join("\n");

await fs.writeFile(outPath, source);
