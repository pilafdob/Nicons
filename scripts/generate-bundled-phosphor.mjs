import fs from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const sourceRoot = path.join(root, "vendor", "phosphor-icons", "SVGs Flat");
const outPath = path.join(root, "src", "BundledPhosphor.ts");
const variants = ["thin", "light", "regular", "bold", "duotone", "fill"];

const bundled = {
  name: "Phosphor Icons",
  variants: {},
};

for (const variant of variants) {
  const folder = path.join(sourceRoot, variant);
  const entries = await fs.readdir(folder, { withFileTypes: true });
  const svgs = {};
  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith(".svg")) continue;
    let slug = entry.name.replace(/\.svg$/i, "");
    const suffix = `-${variant}`;
    if (slug.endsWith(suffix)) slug = slug.slice(0, -suffix.length);
    svgs[slug] = await fs.readFile(path.join(folder, entry.name), "utf8");
  }
  bundled.variants[variant] = Object.fromEntries(Object.entries(svgs).sort());
}

const source = [
  "/* Generated by scripts/generate-bundled-phosphor.mjs. Do not edit directly. */",
  `export const BUNDLED_PHOSPHOR = ${JSON.stringify(bundled)} as const;`,
  "",
].join("\n");

await fs.writeFile(outPath, source);
